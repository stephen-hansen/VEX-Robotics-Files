task usercontrol()
{
	//Start tasks here
  soundPlay = true;
  startTask(pacman);
  while (true)
  {
  	debug = vexRT[Ch3];
		//Drive motor controls
		motor[BL] = vexRT[Ch3];
		motor[FL] = vexRT[Ch3];
		motor[BR] = vexRT[Ch2];
		motor[FR] = vexRT[Ch2];
		//Sound controls
		if (soundPlay == false) {
			if (vexRT[Btn7L] == 1) {
				soundPlay = true;
				startTask(zelda);
			} else if (vexRT[Btn7D] == 1) {
				soundPlay = true;
				startTask(ffvii);
			}	else if (vexRT[Btn7R] == 1) {
				soundPlay = true;
				startTask(smb3);
		  } else if (vexRT[Btn8L] == 1) {
  			soundPlay = true;
  			startTask(smbdeath);
  		} else if (vexRT[Btn8R] == 1) {
  			soundPlay = true;
  			startTask(smw2_map);
			}	else if (vexRT[Btn8D] == 1) {
				soundPlay = true;
				startTask(PPAP);
			}	else if (vexRT[Btn8U] == 1) {
		  	soundPlay = true;
		  	startTask(PokemonGO2);
			}
		}
		if (nVexRCReceiveState & vrXmit2) { //Enable partner controls
			motor[LiftTL] = vexRT[Ch3Xmtr2];
			motor[LiftBL] = vexRT[Ch3Xmtr2];
			motor[LiftTR] = vexRT[Ch3Xmtr2];
			motor[LiftBR] = vexRT[Ch3Xmtr2];
		}	else { //Single controls
			if (vexRT[Btn5U] == 1) {
				if (pFlag == false) {
					pFlag = true;
					nMotorEncoder[LiftBL] = 0;
					nMotorEncoder[LiftBR] = 0;
					startTask(timer);
				}
				motor[LiftTL] = 127;
				motor[LiftBL] = 127;
				motor[LiftTR] = 127;
				motor[LiftBR] = 127;
			}	else if (vexRT[Btn5D] == 1) {
	      if (pFlag == false) {
					pFlag = true;
					nMotorEncoder[LiftBL] = 0;
					nMotorEncoder[LiftBR] = 0;
					startTask(timer);
				}
				motor[LiftTL] = -127;
				motor[LiftBL] = -127;
				motor[LiftTR] = -127;
				motor[LiftBR] = -127;
			} else {
			  if (pFlag == true) {
					pFlag = false;
					stopTask(timer);
					getSpeed();
				}
				motor[LiftTL] = 0;
				motor[LiftBL] = 0;
				motor[LiftTR] = 0;
				motor[LiftBR] = 0;
			}
		}
  }
}
