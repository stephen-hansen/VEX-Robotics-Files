void pre_auton()
{
  bStopTasksBetweenModes = true;
  if (SensorValue[betaTest] == 0) {
  	testVar = 0; //Enable beta features
  	//setRandomSeed();
    } else {
    testVar = 1; //Disable beta features
	}
	//Reset encoders
	nMotorEncoder[LiftBL] = 0;
	nMotorEncoder[LiftBR] = 0;
	startTask(preAutonData);
	startTask(timer);
	while (ready == false) {
		if (nLCDButtons == 1 && pressed == false) {
			pressed = true;
			if (color == "Red") {
				color = "Blue";
			}	else {
	      color = "Red";
			}
			clearLCDLine(0);
			clearLCDLine(1);
		}	else if (nLCDButtons == 2 && pressed == false) {
			pressed = true;
			if (side == "Left") {
				side = "Right";
			}	else {
	      side = "Left";
			}
			clearLCDLine(0);
			clearLCDLine(1);
		}	else if (nLCDButtons == 4 && pressed == false) {
			pressed = true;
			ready = true;
		}	else if (nLCDButtons == 0 && pressed == true) {
			pressed = false;
		}
	}
	stopTask(preAutonData);
	stopTask(timer);
	bLCDBacklight = true;
	clearLCDLine(0);
	clearLCDLine(1);
	if ((time % 10) <= 3) {
		sprintf(line1, "AUTO SET!");
		sprintf(line2, "READY");
	}	else if ((time % 10) <= 5) {
		sprintf(line1, "AUTO GET!");
		sprintf(line2, "REEFY");
	}	else if ((time % 10) <= 7) {
		sprintf(line1, "SET AUTO!");
		sprintf(line2, "GRAVY");
	}	else if ((time % 10) <= 8) {
		sprintf(line1, "SORRY BRO!");
		sprintf(line2, "- YOUNG");
	}	else if ((time % 10) <= 9) {
		sprintf(line1, "HEY, THAT'S");
		sprintf(line2, "PRETTY GOOD!");
	}
	displayLCDPos(0, 0);
	displayNextLCDString(line1);
	displayLCDPos(1, 0);
	displayNextLCDString(line2);
	wait1Msec(2000);
	clearLCDLine(0);
	clearLCDLine(1);
	playSound(soundBlip);
	playSound(soundException);
	playSound(soundBeepBeep);
	playSound(soundLowBuzz);
}

