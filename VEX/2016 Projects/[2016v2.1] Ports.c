task usercontrol()
{
	clearDebugStream();
	stopTask(timer);
	startTask(encoderValues);
	//Reset encoders
	nMotorEncoder[BR] = 0;
	nMotorEncoder[BL] = 0;
	//Reset motors
	motor[BL] = 0;
	motor[BR] = 0;
	motor[FL] = 0;
	motor[FR] = 0;
	//Start tasks here
	stopTask(timer);
	startTask(timer);
	soundPlay = true;
	startTask(balloon);
	while (true)
	{

		//Claw controls
		if (vexRT[Btn5U] == 1) {
			motor[claw] = 127;
		}	else if (vexRT[Btn6U] == 1) {
			motor[claw] = -127;
		} else {
			motor[claw] = 0;
		}

		//Lift controls
		if (vexRT[Btn5D] == 1) {
			motor[liftTL] = 127;
			motor[liftML] = 127;
			motor[liftTR] = 127;
			motor[liftMR] = 127;
			motor[liftBLR] = 127;
		}	else if (vexRT[Btn6D] == 1) {
			motor[liftTL] = -127;
			motor[liftML] = -127;
			motor[liftTR] = -127;
			motor[liftMR] = -127;
			motor[liftBLR] = -127;
		} else {
			motor[liftTL] = 0;
			motor[liftML] = 0;
			motor[liftTR] = 0;
			motor[liftMR] = 0;
			motor[liftBLR] = 0;
		}

		//Drive motor controls
		if (vexRT[Btn8D] == 0 && vexRT[Btn8U] == 0 && vexRT[Btn8L] == 0 && vexRT[Btn8R] == 0) {
			if (testVar == 1) {
				if (abs(vexRT[Ch3]) <= abs(deadzone3)) {
					motor[BL] = 0;
					motor[FL] = 0;
					}	else {
					motor[BL] = vexRT[Ch3];
					motor[FL] = vexRT[Ch3];
				}
				if (abs(vexRT[Ch2]) <= abs(deadzone2)) {
					motor[BR] = 0;
					motor[FR] = 0;
					} else {
					motor[BR] = vexRT[Ch2];
					motor[FR] = vexRT[Ch2];
				}
				} else { //Motion controls
				if (abs(vexRT[AccelX]) > abs(vexRT[AccelY])) { //Turning Activated
					motor[BL] = vexRT[AccelX];
					motor[FL] = vexRT[AccelX];
					motor[BR] = -vexRT[AccelX];
					motor[FR] = -vexRT[AccelX];
					} else { //Moving Activated
					motor[BL] = -vexRT[AccelY];
					motor[FL] = -vexRT[AccelY];
					motor[BR] = -vexRT[AccelY];
					motor[FR] = -vexRT[AccelY];
				}
			}
		}
		//Sound controls
		if (soundPlay == false) {
			if (vexRT[Btn7R] == 1) {
				soundPlay = true;
				startTask(PokemonGO2);
				} else if (vexRT[Btn7D] == 1) {
				soundPlay = true;
				startTask(PPAP);
			} else if (vexRT[Btn7L] == 1) {
				soundPlay = true;
				startTask(smb3);
			} else if (vexRT[Btn7U] == 1) {
				soundPlay = true;
				startTask(smw2_map);
			}
		}
		if (vexRT[Btn8D] == 1) {
			mode = 1;
			motor[BR] = -127;
			motor[BL] = -127;
			motor[FL] = -127;
			motor[FR] = -127;
			while (abs(nMotorEncoder[BR]) < drive1SquareEncVal) {
			}
			nMotorEncoder[BR] = 0;
			motor[BR] = 0;
			motor[BL] = 0;
			motor[FL] = 0;
			motor[FR] = 0;
			}	else if (vexRT[Btn8U] == 1) {
			mode = 2;
			motor[BR] = 127;
			motor[BL] = 127;
			motor[FL] = 127;
			motor[FR] = 127;
			while (abs(nMotorEncoder[BR]) < drive1SquareEncVal) {
			}
			nMotorEncoder[BR] = 0;
			motor[BR] = 0;
			motor[BL] = 0;
			motor[FL] = 0;
			motor[FR] = 0;
			}	else if (vexRT[Btn8L] == 1) {
			mode = 3;
			turning(-debugSpeed);
			} else if (vexRT[Btn8R] == 1) {
			mode = 4;
			turning(debugSpeed);
			} else { //AutoGenerator
			if (mode != 0) {
				if (mode == 1) {
					writeDebugStreamLine("while (nMotorEncoder[BR] > %d) {", nMotorEncoder[BR]);
					writeDebugStreamLine("	moving(-auSpeed);");
					writeDebugStreamLine("}");
					writeDebugStreamLine("moving(0);");
					writeDebugStreamLine("wait1Msec(100);");
					}	else if (mode == 2) {
					writeDebugStreamLine("while (nMotorEncoder[BR] < %d) {", nMotorEncoder[BR]);
					writeDebugStreamLine("	moving(auSpeed);");
					writeDebugStreamLine("}");
					writeDebugStreamLine("moving(0);");
					writeDebugStreamLine("wait1Msec(100);");
					} else if (mode == 3) {
					writeDebugStreamLine("while (nMotorEncoder[BR] < %d) {", nMotorEncoder[BR]);
					writeDebugStreamLine("	right(auSpeed);");
					writeDebugStreamLine("}");
					writeDebugStreamLine("right(0);");
					writeDebugStreamLine("wait1Msec(100);");
					} else if (mode == 4) {
					writeDebugStreamLine("while (nMotorEncoder[BL] < %d) {", nMotorEncoder[BL]);
					writeDebugStreamLine("	left(auSpeed);");
					writeDebugStreamLine("}");
					writeDebugStreamLine("left(0);");
					writeDebugStreamLine("wait1Msec(100);");
				}
				mode = 0;
			}
		}
	}
}
