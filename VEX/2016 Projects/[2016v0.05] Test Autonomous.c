void setRandomSeed() {
	clearLCDLine(0);
	clearLCDLine(1);
	check = false;
	click = false;
	while (check == false) {
		bLCDBacklight = true;									// Turn on LCD Backlight
		displayLCDPos(0,0);
		string display1;
		sprintf(display1, "SET RANDSEED");
		displayNextLCDString(display1);
		displayLCDPos(1,0);
		string display2;
		sprintf(display2, "SEED: %4d", seed);
		displayNextLCDString(display2);
		if (nLCDButtons == 1 && click == false) {
			click = true;
			seed = seed - 1;
		} else if (nLCDButtons == 4 && click == false) {
			click = true;
			seed = seed + 1;
		} else if (nLCDButtons == 2) {
			check = true;
		} else if (nLCDButtons == 0 && click == true) {
			click = false;
		}
	}
	clearLCDLine(0);
	clearLCDLine(1);
	bLCDBacklight = true;									// Turn on LCD Backlight
	displayLCDPos(0,0);
	string display1;
	sprintf(display1, "RANDSEED SET!");
	displayNextLCDString(display1);
	displayLCDPos(1,0);
	string display2;
	sprintf(display2, "SEED: %4d", seed);
	displayNextLCDString(display2);
}

void dumbAutonomous() {
	srand(seed);
	timeleft = 15000;
	startTask(smb3);
	while (timeleft > 0) {
		clearLCDLine(0);
		clearLCDLine(1);
		bLCDBacklight = true;									// Turn on LCD Backlight
		displayLCDPos(0,0);
		string display1;
		sprintf(display1, "RANDAUTO");
		displayNextLCDString(display1);
		displayLCDPos(1,0);
		string display2;
		sprintf(display2, "STEP: %2d", step);
		displayNextLCDString(display2);
		lspeed = random(254) - 127;
		rspeed = random(254) - 127;
		motor[BL] = lspeed;
		motor[FL] = lspeed;
		motor[BR] = rspeed;
		motor[FR] = rspeed;
		timechosen = random(timeleft);
		timeleft = timeleft - timechosen;
		wait1Msec(timechosen);
		step = step + 1;
	}
	motor[BL] = 0;
	motor[FL] = 0;
	motor[BR] = 0;
	motor[FR] = 0;
	clearLCDLine(0);
	clearLCDLine(1);
	bLCDBacklight = true;									// Turn on LCD Backlight
	displayLCDPos(0,0);
	string display1;
	sprintf(display1, "RANDAUTO");
	displayNextLCDString(display1);
	displayLCDPos(1,0);
	string display2;
	sprintf(display2, "COMPLETE");
	displayNextLCDString(display2);
}

