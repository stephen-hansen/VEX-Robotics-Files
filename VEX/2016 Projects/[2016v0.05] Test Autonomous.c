task usercontrol()
{
	//Start tasks here
  soundPlay = true;
  startTask(pacman);
  while (true)
  {
		//Drive motor controls
		if (testVar == 1) { //Default tank controls
			motor[BL] = vexRT[Ch3];
			motor[FL] = vexRT[Ch3];
			motor[BR] = vexRT[Ch2];
			motor[FR] = vexRT[Ch2];
		} else { //Beta motion controls
			if (abs(vexRT[AccelX]) > abs(vexRT[AccelY])) { //Turning Activated
				motor[BL] = vexRT[AccelX];
				motor[FL] = vexRT[AccelX];
				motor[BR] = -vexRT[AccelX];
				motor[FR] = -vexRT[AccelX];
	 	  } else { //Moving Activated
		  	motor[BL] = -vexRT[AccelY];
				motor[FL] = -vexRT[AccelY];
				motor[BR] = -vexRT[AccelY];
				motor[FR] = -vexRT[AccelY];
			}
		}
		//Sound ctonrols
		if (soundPlay == false) {
			if (vexRT[Btn7L] == 1) {
				soundPlay = true;
				startTask(zelda);
			} else if (vexRT[Btn7D] == 1) {
				soundPlay = true;
				startTask(ffvii);
			}	else if (vexRT[Btn7R] == 1) {
				soundPlay = true;
				startTask(smb3);
		  } else if (vexRT[Btn8L] == 1) {
  			soundPlay = true;
  			startTask(smbdeath);
  		} else if (vexRT[Btn8R] == 1) {
  			soundPlay = true;
  			startTask(smw2_map);
			}	else if (vexRT[Btn8D] == 1) {
				soundPlay = true;
				startTask(dkc);
			}	else if (vexRT[Btn8U] == 1) {
		  	soundPlay = true;
		  	startTask(PokemonGO2);
			}
		}
		if (nVexRCReceiveState & vrXmit2) { //Enable partner controls
			motor[LiftTL] = vexRT[Ch3Xmtr2];
			motor[LiftBL] = vexRT[Ch3Xmtr2];
			motor[LiftTR] = vexRT[Ch3Xmtr2];
			motor[LiftBR] = vexRT[Ch3Xmtr2];
		}	else { //Single controls
			if (vexRT[Btn5U] == 1) {
				motor[LiftTL] = 127;
				motor[LiftBL] = 127;
				motor[LiftTR] = 127;
				motor[LiftBR] = 127;
			}	else if (vexRT[Btn5D] == 1) {
				motor[LiftTL] = -127;
				motor[LiftBL] = -127;
				motor[LiftTR] = -127;
				motor[LiftBR] = -127;
			} else {
				motor[LiftTL] = 0;
				motor[LiftBL] = 0;
				motor[LiftTR] = 0;
				motor[LiftBR] = 0;
			}
		}
  }
}
