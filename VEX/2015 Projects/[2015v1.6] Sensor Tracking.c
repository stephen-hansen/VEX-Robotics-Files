int LIGHT_VALUE = 2500; //The value of light the robot needs or less to shoot
int DELAY_TIME = 100; //Time between detection of ball and shoot
int TIMER = 0;

task update () {
	while (true) {
		wait1Msec(1000);
		TIMER = TIMER + 1;
	}
}

void shoot (int speed, int time) { //Function to shoot spheres from launcher
	wait1Msec(DELAY_TIME);
	motor[shooter1] = speed;
	motor[shooter2] = speed;
	wait1Msec(time);
	motor[shooter1] = 0;
	motor[shooter2] = 0;
}

void uShoot() {
	motor[shooter1] = vexRT[Ch2];
	motor[shooter2] = vexRT[Ch2];
}

void uTurn() {
	motor[turntable] = vexRT[Ch4];
}

void uShootMotion() {
	if (vexRT[AccelY] < 0) {
		motor[shooter1] = ((-vexRT[AccelY] * 127)/90);
		motor[shooter2] = ((-vexRT[AccelY] * 127)/90);
	} else {
		motor[shooter1] = 0;
		motor[shooter2] = 0;
	}
}

