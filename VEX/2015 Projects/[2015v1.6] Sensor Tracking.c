/////////////////////////////////////////////////////////////////////////////////////////

task usercontrol() {
	startTask(update);
	while (true) {
		uTurn();
		if (SensorValue[line] <= LIGHT_VALUE) {
  		if (vexRT[Btn5D] == 1 || vexRT[Btn5U] == 1) {
  			stopTask(nodebug);
  			stopTask(buttonDebug);
  			stopTask(sensorDebug);
  			stopTask(otherDebug);
  			stopTask(debug2);
  			if (SensorValue[motion] > 1) {
  				startTask(debug);
  				uShoot();
  			}
  			else if (SensorValue[motion] == 0) {
  				startTask(debugMotion);
  				uShootMotion();
  			}
  		}
  	}
  	if (vexRT[Btn6D] == 1 || vexRT[Btn6U] == 1) {
  		stopTask(nodebug);
  		stopTask(buttonDebug);
  		stopTask(sensorDebug);
  		stopTask(otherDebug);
  		stopTask(debug2);
  		if (SensorValue[motion] > 1) {
  			startTask(debug);
  			uShoot();
  		}
  		else if (SensorValue[motion] == 0) {
  			startTask(debugMotion);
  			uShootMotion();
  		}
  	}
  	else if (vexRT[Btn7U] == 1 && TIMER >= 100) {
  		stopTask(nodebug);
  		stopTask(buttonDebug);
  		stopTask(sensorDebug);
  		stopTask(otherDebug);
  		stopTask(debug);
  		stopTask(debugMotion);
  		startTask(debug2);
  	}
  	else if (vexRT[Btn8U] == 1 || nLCDButtons == 1){
  		stopTask(debug);
  		stopTask(buttonDebug);
  		stopTask(sensorDebug);
  		stopTask(otherDebug);
  		stopTask(debug2);
  		stopTask(debugMotion);
  		startTask(nodebug);
  	}
  	else if (vexRT[Btn8R] == 1 || nLCDButtons == 2) {
  		stopTask(debug);
  		stopTask(nodebug);
  		stopTask(sensorDebug);
  		stopTask(otherDebug);
  		stopTask(debug2);
  		stopTask(debugMotion);
  		startTask(buttonDebug);
  	}
  	else if (vexRT[Btn8D] == 1 || nLCDButtons == 4) {
  		stopTask(debug);
  		stopTask(nodebug);
  		stopTask(otherDebug);
  		stopTask(buttonDebug);
  		stopTask(debug2);
  		stopTask(debugMotion);
  		startTask(sensorDebug);
  	}
  	else if (vexRT[Btn8L] == 1 || nLCDButtons == 3) {
  		stopTask(debug);
  		stopTask(nodebug);
  		stopTask(buttonDebug);
  		stopTask(sensorDebug);
  		stopTask(debug2);
  		stopTask(debugMotion);
  		startTask(otherDebug);
  	}
	}
}
