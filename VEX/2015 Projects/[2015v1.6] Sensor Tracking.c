task autonomous() {
	stopTask(sensorDebug);
  while (true) {
  	if (SensorValue[line] <= LIGHT_VALUE) {
  		stopTask(nodebug);
  		startTask(debug);
  		shoot(127,4000);
  		stopTask(debug);
  	}
  	else {
  		stopTask(debug);
  		startTask(nodebug);
  	}
  }
}

